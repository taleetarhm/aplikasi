				<h3>SEMARANG NIGHT CARNIVAL (SNC)</h3>
				<p>Selain itu acara tahunan terbesar di Kota Semarang ini juga akan disaksikan 11 duta besar Negara yang akan ikut turnamen karate se-Asia.

Untuk tema Semarang Night Carnival tahun ini berkonsep “Paras Semarang” yang menggambarkan wajah Semarang dalam bentuk ikon-ikon terpopuler. Wajah Semarang tersebut terlambangkan ke dalam 4 hal yaitu kekayaan kuliner, flora, fauna dan ikon bangunan di kota ini. Kekayaan kuliner digambarkan dengan lunpia dan bandeng presto, Sedang flora fauna disimbolkan dengan bunga sepatu dan burung blekok yang mayoritas tumbuh subur di Semarang. Sementara ikon bangunan yang ada seperti Tugu Muda dan Lawang Sewu.

Kabag Humas dan Protokol Semarang, Achyani mengatakan gelaran ini digelar Pemkot secara gratis sehingga masyarakat diharapkan untuk tidak melewatkan dengan menyaksikan secara langsung. Selama berlangsungnya event ini, pihak Pemkot Semarang akan melakukan sistem buka tutup jalan yang dilewati peserta SNC. “Pihak kami melalui Dinas Perhubungan akan melihat kondisi jalan terlebih dahulu. Apabila kondisi memungkinkan, jalan tidak kami tutup namun peserta SNC masih bisa melewatinya. Begitu juga sebaliknya kami akan menutup jalan sebagian” terangnya dalam jumpa pers.

Ia pun menambahkan Dinas Perhubungan telah menyiapkan beberapa kantong parkir untuk memudahkan warga masyarakat yang akan menonton event tahunan tersebut. Kantong-kantong parkir tersebut berada di dalam gedung maupun parkir tepi jalan umum (on street parking). “untuk parkir tepi kami sediakan di Jalan Piere Tendean, Jalan MH. Thamrin, Imam Bonjol, Sendowo dan juga Gajahmada”, jelasnya.

Pihaknya berharap masyarakat dapat memarkirkan kendaraannya sesuai dengan tempat yang telah disediakan secara tertib. Dengan demikian, diharapkan acara akan berjalan dengan lancar dan memberikan suasana aman dan nyaman khususnya bagi warga luar kota maupun wisatawan yang datang untuk menyaksikan Semarang Night Carnival.</p><br>
